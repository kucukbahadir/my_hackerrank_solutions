int max_of_four(int a, int b, int c, int d) {
    int maxNumber = a;

    if (b > maxNumber) maxNumber = b;
    if (c > maxNumber) maxNumber = c;
    if (d > maxNumber) maxNumber = d;
    
    return maxNumber;
}
